package cz.cvut.fit.tjv.cashier_application.api.model;

/**
 * Data Transfer Object class for MenuItem entity type.
 */
public class MenuItemDto {
    private int id;
    private String name;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
